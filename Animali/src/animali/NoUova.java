package animali;

public class NoUova extends Animale {
	private int numeroCuccioli;
	private boolean monogamo;
	public int getNumeroCuccioli() {
		return numeroCuccioli;
	}
	public void setNumeroCuccioli(int numeroCuccioli) {
		this.numeroCuccioli = numeroCuccioli;
	}
	public boolean isMonogamo() {
		return monogamo;
	}
	public void setMonogamo(boolean monogamo) {
		this.monogamo = monogamo;
	}
	
}
